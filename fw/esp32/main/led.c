// Names of the animations
const char *const led_cb_name[] = {"rainbow", "green",    "sp4rkle",
                                   "s3cr3t",  "sp1n2W1n", "F1r3"};

// Callbacks
void *led_cb_array[] = {led_rainbow_cb, led_green_cb,    led_sparkle_cb,
                        led_s3cre3t_cb, led_sp1n2W1n_cb, led_fire_cb};

/**
  led_task
  @brief Task that updates the LEDs
  @parms pvParameter -> rgb_led_ctx: RGB Led structure
  @return -
*/
void led_task(void *pvParameter) {
  rgb_led_ctx_t *rgb_led_ctx = (rgb_led_ctx_t *)pvParameter;
  uint32_t t = 0;
  uint8_t shader;
  esp_err_t err;
  err = led_get_saved(&shader);
  if (err != ESP_OK) {
    ESP_LOGE(TAG, "[x] No previous shader configuration set.");
    shader = 0;
  }
  ESP_LOGI(TAG, "[+] LED Task started.");
  ESP_LOGI(TAG, "[+] LED Task calling %s (%d)", led_cb_name[shader], shader);
  while (1) {
    t++;
    led_shader(t, rgb_led_ctx);
    vTaskDelay(10 / portTICK_PERIOD_MS);
  }

  ESP_LOGE(TAG, "[x] LED Task exited.");
}

/**
  led_init
  @brief Initialize RMT and sets up the SK6805 LED chain
  @parms rgb_led_ctx: RGB Led structure
  @return -
*/
void led_init(rgb_led_ctx_t *rgb_led_ctx) {
  uint8_t shader;
  esp_err_t err;
  rgb_led_ctx->tx_chan_config.clk_src =
      RMT_CLK_SRC_DEFAULT; // select source clock
  rgb_led_ctx->tx_chan_config.gpio_num = BOARD_LED_GPIO_NUM;
  rgb_led_ctx->tx_chan_config.mem_block_symbols =
      64; // increase the block size can make the LED less flickering
  rgb_led_ctx->tx_chan_config.resolution_hz = LED_STRIP_RESOLUTION_HZ;
  rgb_led_ctx->tx_chan_config.trans_queue_depth =
      4; // set the number of transactions that can be pending in the background

  rgb_led_ctx->tx_config.loop_count = 0;

  rgb_led_ctx->led_chan = 0;
  rgb_led_ctx->led_encoder = NULL;

  err = led_get_saved(&shader);
  if (err != ESP_OK) {
    ESP_LOGE(TAG, "[x] No previous shader configuration set.");
    shader = 0;
  }

  rgb_led_ctx->led_cb = led_cb_array[shader];

  ESP_ERROR_CHECK(
      rmt_new_tx_channel(&rgb_led_ctx->tx_chan_config, &rgb_led_ctx->led_chan));
  ESP_LOGI(TAG, "[+] Install led strip encoder");

  led_strip_encoder_config_t encoder_config = {
      .resolution = LED_STRIP_RESOLUTION_HZ,
  };

  rgb_led_ctx->led_encoder = NULL;

  ESP_ERROR_CHECK(
      rmt_new_led_strip_encoder(&encoder_config, &rgb_led_ctx->led_encoder));

  ESP_LOGI(TAG, "[+] Enable RMT TX channel");
  ESP_ERROR_CHECK(rmt_enable(rgb_led_ctx->led_chan));
}

/**
  led_commit
  @brief Sends data to the LEDs
  @parms rgb_led_ctx: RGB Led structure
  @return -
*/
void led_commit(rgb_led_ctx_t *rgb_led_ctx, uint8_t *led_strip_pixels) {
  ESP_ERROR_CHECK(rmt_transmit(rgb_led_ctx->led_chan, rgb_led_ctx->led_encoder,
                               led_strip_pixels, BOARD_LED_NUMBERS * 3,
                               &rgb_led_ctx->tx_config));
  ESP_ERROR_CHECK(rmt_tx_wait_all_done(rgb_led_ctx->led_chan, portMAX_DELAY));
}

/**
  led_update
  @brief alias of commit
  @parms rgb_led_ctx: RGB Led structure
  @return -
*/
void led_update(rgb_led_ctx_t *rgb_led_ctx) {
  led_commit(rgb_led_ctx, rgb_led_ctx->led_array);
}

/**

*/
void led_rainbow_cb(uint16_t t, uint8_t ledindex, void *ctx) {
  rgb_led_ctx_t *rgb_led_ctx = (rgb_led_ctx_t *)ctx;
  uint8_t r;
  uint8_t g;
  uint8_t b;
  led_strip_hsv2rgb((t + ledindex * (90 / BOARD_LED_NUMBERS)) % 360, 100, 100,
                    &r, &g, &b);
  rgb_led_ctx->led_array[ledindex * 3 + 0] = g;
  rgb_led_ctx->led_array[ledindex * 3 + 1] = r;
  rgb_led_ctx->led_array[ledindex * 3 + 2] = b;
}

